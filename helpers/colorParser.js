// Color parser function
const colorParser = (colorName) => {
    // Convert the input colorName to lowercase and trim it
    colorName = colorName.toLowerCase().trim();

    // Look for the color in the predefined list
    const color = colorNames[colorName];

    // If the color exists, perform conversion
    if (color) {
        const rgb = hexToRgb(color.hex); // Convert HEX to RGB
        const hsl = hexToHsl(color.hex); // Convert HEX to HSL

        // Return all necessary color data (HEX, RGB, HSL)
        return {
            hex: color.hex,
            rgb: rgb,
            hsl: hsl
        };
    }

    // Return an error message if the color is not found
    return { error: `Invalid color name: "${colorName}". Please try another.` };
};

